<p style="margin-left: 400px;margin-right: 400px" >Đã thành lệ hằng năm, cứ đến tháng 11 là mình sẽ đem lòng nhớ Tết.</p>



<p style="margin-left: 400px;margin-right: 400px">Tối hôm qua Gấu béo chở mình đi bát phố, mình lẩm nhẩm hát đoạn mình thích nhất trong bài Xuân Họp Mặt.</p>



<p style="margin-left: 400px;margin-right: 400px">“Vui mùa xuân năm nay gặp nhau&nbsp;<br>Nhớ khi nao lúc ra đi&nbsp;<br>Cầm tay hẹn câu chờ nhau&nbsp;<br>Đến bên cầu nhắn xuân sau.”</p>



<p style="margin-left: 400px;margin-right: 400px" >Đoản khúc này, tưởng chừng như mình đã thầm hát tặng cho rất nhiều người mỗi lần xuân đến. Rất nhiều. Từ những ngày đầu tiên đặt chân đến Mỹ, mơ đến một ngày xuân lời hát thành sự thật, có thể về lại non nước năm xưa, cầm tay người tri kỷ hoa niên mà nói. “Ta về rồi.”</p>



<p style="margin-left: 400px;margin-right: 400px">Khởi từ ngày ấy trở đi, những người mình hẹn mùa xuân gặp mặt lại càng thêm nhiều. Z, I, L, M, N, và rất nhiều những người bạn tuy không thân nhưng đã từng trải qua với mình những cuộc rong chơi rực rỡ hoàng hoa. Giá mà có thể gặp lại. Giá mà một xuân xanh nào đó.</p>



<p style="margin-left: 400px;margin-right: 400px">Đến gần cái Tết thứ ba ở Việt Nam, mình mới nhận ra vì sao không thể ngừng nỗi bâng khuâng : Tết bây giờ mình có Việt Nam, nhưng Tết năm xưa mình có tuổi trẻ.</p>



<p style="margin-left: 400px;margin-right: 400px">Và rồi Tết vẫn sẽ đến. Như bất kỳ người nào, mình cũng quên đi nhiều những kỷ niệm xưa, quên mất những cảm xúc mà lúc trẻ nghĩ rằng sẽ trăm năm tạc dạ ghi lòng.</p>



<p style="margin-left: 400px;margin-right: 400px">Khi biết mình dễ quên đến thế thì lại càng trân quý hiện tại hơn. Năm xưa đến tuổi ẩm ương, mình rốt cuộc đã vượt qua cơn khủng hoảng hiện sinh rất nhanh. Trong lúc chúng bạn còn đau đáu tìm lẽ sống, mình đã nghĩ như này: Nếu thực sự sinh ra không có mục đích gì đặc biệt, vậy thì có gì để sợ mà không sống cho oanh oanh liệt liệt một phen này? Vì có gì để mất đâu. Bản thân càng vô nghĩa thì càng phải sống cho thoả, kỷ niệm càng dễ phai thì càng phải yêu những gì của hôm nay nhiều hơn.</p>



<p style="margin-left: 400px;margin-right: 400px" >Vậy cho nên, khi mỗi mùa Tết đến, mình luôn mong ước được “gặp lại nhau bên cầu”. Nhưng hơn vậy nữa là được cầm tay những người của hiện tại. Dẫu biết vận đổi sao dời nhưng hôm nay vẫn mong không bao giờ có ly tan.</p>


